# 일반적으로 1부터 100까지 숫자에 대해 적용합니다.
# 범위를 변경하려면 range(시작_숫자, 끝_숫자 + 1) 부분을 수정하세요.
for number in range(1, 101):
    # 15의 배수인지 가장 먼저 확인합니다.
    # 15의 배수는 3의 배수 조건과 5의 배수 조건 모두에 해당되므로,
    # 이 조건을 먼저 체크해야 정확하게 "fizzbuzz"를 출력할 수 있습니다.
    if number % 15 == 0:
        print("fizzbuzz") # 15의 배수일 때 "fizzbuzz" 출력
    # 15의 배수가 아닌 경우에만 3의 배수인지 확인합니다.
    elif number % 3 == 0:
        print("fizz")     # 3의 배수일 때 "fizz" 출력
    # 15의 배수도, 3의 배수도 아닌 경우에만 5의 배수인지 확인합니다.
    elif number % 5 == 0:
        print("buzz")     # 5의 배수일 때 "buzz" 출력
    # 위의 어떤 조건에도 해당되지 않는 경우 (3, 5, 15의 배수가 아닐 때)
    else:
        print(number)     # 숫자를 그대로 출력

